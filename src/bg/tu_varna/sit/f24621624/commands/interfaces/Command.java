package bg.tu_varna.sit.f24621624.commands.interfaces;

/**
 * Главен интерфейс, който всяка команда в проекта трябва да имплементира.
 */
public interface Command
{
    /**
     * Връща текстовото име на командата (например "open", "exit").
     * @return името на командата
     */
    String getName();
    /**
     * Връща кратко описание как се използва командата.
     * @return описанието на командата
     */
    String getDescription();
    /**
     * Изпълнява същинската логика на командата с подадените от конзолата аргументи.
     * @param args масив от параметри, въведени от потребителя
     * @return съобщение за резултата от изпълнението (успех или грешка)
     */
    String execute(String[] args);
}
